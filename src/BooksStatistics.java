        public void Update() {
            String sql = "Select * from Books";
    try{
        pst = conn.prepareStatement(sql);
        rs = pst.executeQuery(); 
        
        Object [] columnData = new Object [6];
         while(rs.next()){  
            columnData[0] = rs.getString("Grade");
            columnData[1] = rs.getString("Name"); 
            columnData[2] = rs.getString("NumNew"); 
            columnData[3] = rs.getString("PriceNew"); 
            columnData[4] = rs.getString("NumOld"); 
            columnData[5] = rs.getString("PriceOld"); 
            model.addRow(columnData);


    }
    }
    catch(Exception e){
        JOptionPane.showMessageDialog(null, e);
    }
}
